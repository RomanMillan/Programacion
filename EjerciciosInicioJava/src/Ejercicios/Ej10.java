package Ejercicios;

/*S
 * Escribe los números del 100 al 1 utilizando la instrucción for
 * */
public class Ej10 {

	public static void main(String[] args) {
		
		num100Al1();
	}
	//metodo que imprime los num del 100 al 1
	public static void num100Al1() {
		for (int i = 100;i>=1;i--) {
			System.out.println(i);
		}
	}
}
