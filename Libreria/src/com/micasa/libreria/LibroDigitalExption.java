package com.micasa.libreria;

public class LibroDigitalExption extends Exception {

	public LibroDigitalExption() {
		super();
		
	}

	public LibroDigitalExption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public LibroDigitalExption(String message, Throwable cause) {
		super(message, cause);
		
	}

	public LibroDigitalExption(String message) {
		super(message);
		
	}

	public LibroDigitalExption(Throwable cause) {
		super(cause);
		
	}

}
