package com.jacaranda.utilities;

public class PuebloException extends Exception {

	public PuebloException() {
		super();
		
	}

	public PuebloException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PuebloException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PuebloException(String message) {
		super(message);
	
	}

	public PuebloException(Throwable cause) {
		super(cause);
	
	}

}
