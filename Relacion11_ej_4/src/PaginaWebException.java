
public class PaginaWebException extends Exception {

	public PaginaWebException() {
		super();

	}

	public PaginaWebException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public PaginaWebException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public PaginaWebException(String message) {
		super(message);

	}

	public PaginaWebException(Throwable cause) {
		super(cause);

	}

}
