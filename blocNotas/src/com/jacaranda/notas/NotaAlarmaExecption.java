package com.jacaranda.notas;

public class NotaAlarmaExecption extends Exception {

	public NotaAlarmaExecption() {
		super();
		
	}

	public NotaAlarmaExecption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public NotaAlarmaExecption(String message, Throwable cause) {
		super(message, cause);

	}

	public NotaAlarmaExecption(String message) {
		super(message);

	}

	public NotaAlarmaExecption(Throwable cause) {
		super(cause);

	}

}
